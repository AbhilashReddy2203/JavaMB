-- abs() : returns absolute value

select abs(-12);

-- sqrt(n) : Returns sqrt value 

select sqrt(49);

-- power(n,m) : Returns n power m value 

select power(2,3);

-- ceiling() : returns greatest integer value. 

select ceiling(12.00000001);

-- floor() : Returns smallest integer value 

select floor(12.9999999);

-- round() : used to round the value to the particular digits 

select round(8875.8776,3);
select round(8875.8776,2);
select round(8875.8776,1);
select round(8875.8776,0);
select round(8875.8776,-1);
select round(8875.8776,-2);
select round(8875.8776,-3);